// Class Name should be same as the filename
public class HelloWorld {
    // main() - Entry point for our java program
    // public - Anyone can access it
    // static - You can run this method without creating an instance of HelloWorld
    // void - This method does not return any value
    public static void main(String[] args) {
        // System - pre-defined class
        // out - represents the output of your program
        // println - Used to print a line
        System.out.println("Hello World");
    }
}